"""TickEngine — one virtual day per step(). Drives rules + agents."""

from __future__ import annotations

import random
from dataclasses import dataclass

from living_world.core.world import World
from living_world.memory.memory_store import AgentMemoryStore
from living_world.persistence import Repository
from living_world.agents.chronicler import Chronicler
from living_world.agents.conscience import ConsciousnessLayer
from living_world.agents.dialogue import DialogueGenerator
from living_world.agents.emergent import EmergentEventProposer
from living_world.agents.narrator import Narrator
from living_world.agents.perception import SubjectivePerception
from living_world.agents.planner import AgentPlanner
from living_world.agents.self_update import AgentSelfUpdate
from living_world.rules.decay import decay_needs_and_emotions
from living_world.rules.heat import hot_tiles as _rule_hot_tiles
from living_world.rules.consequences import ConsequenceEngine
from living_world.rules.historical_figures import HistoricalFigureRegistry
from living_world.rules.interactions import InteractionEngine
from living_world.rules.movement import MovementPolicy
from living_world.rules.resolver import EventResolver
from living_world.rules.storyteller import TileStoryteller
from living_world.tick_logger import TickLogger
from living_world.world_pack import WorldPack


@dataclass
class TickStats:
    tick: int
    proposals: int = 0
    events_realized: int = 0
    spotlight_candidates: int = 0
    promotions: int = 0
    movements: int = 0
    reactions: int = 0      # events generated by consequence chain
    tier1: int = 0
    tier3: int = 0


class TickEngine:
    """Glues rules + agents + memory + persistence into one tick loop."""

    def __init__(
        self,
        world: World,
        packs: list[WorldPack],
        seed: int = 42,
        narrator: Narrator | None = None,
        repository: Repository | None = None,
        memory: AgentMemoryStore | None = None,
        snapshot_every_ticks: int = 10,
        reflect_every_ticks: int = 7,
    ) -> None:
        self.world = world
        self.packs = {p.pack_id: p for p in packs}
        self.rng = random.Random(seed)
        self.resolver = EventResolver(world, random.Random(seed + 1))
        self.consciousness: ConsciousnessLayer | None = None  # wired by factory
        self.hf_registry = HistoricalFigureRegistry(world)
        self.movement = MovementPolicy(world, random.Random(seed + 2))
        self.interactions = InteractionEngine(world, random.Random(seed + 3))
        self.consequences = ConsequenceEngine(world, random.Random(seed + 4))
        self.narrator = narrator or Narrator()
        self.repository = repository
        self.memory = memory
        self.snapshot_every_ticks = snapshot_every_ticks
        self.reflect_every_ticks = reflect_every_ticks
        self.tick_logger: TickLogger | None = None
        self.agent_planner: AgentPlanner | None = None
        self.plan_every_ticks: int = 7
        self.plan_hf_only: bool = True
        # A→B dialogue turn that mutates affinity + beliefs.
        # Factory sets dialogue_generator + conversation_loop_enabled.
        self.dialogue_generator: DialogueGenerator | None = None
        self.conversation_loop_enabled: bool = False
        # Chronicler (说书人) — observes, never steers.
        self.chronicler: Chronicler | None = None
        self.chronicle_every_ticks: int = 14
        self._last_chronicle_tick: int = 0
        # Emergent event proposer — LLM invents novel events on hot tiles.
        self.emergent_proposer: EmergentEventProposer | None = None
        self.emergent_max_per_tick: int = 2
        # Subjective perception: rewrite events from each participant's POV
        # before storing in memory. Only fires for important events to keep
        # cost bounded (1 LLM call per participant per important event).
        self.perception: SubjectivePerception | None = None
        self.perception_threshold: float = 0.5
        # AgentSelfUpdate: after important events, LLM speaks AS the
        # participant and reports how their inner state shifted (attributes,
        # needs, emotions, beliefs, goal, motivations, reflection).
        self.self_update: AgentSelfUpdate | None = None
        self.self_update_threshold: float = 0.5

        # one storyteller per tile, using its primary pack's storyteller config
        self.storytellers: dict[str, TileStoryteller] = {}
        for tile in world.all_tiles():
            pack = self.packs.get(tile.primary_pack)
            if pack is None:
                continue
            self.storytellers[tile.tile_id] = TileStoryteller(
                tile=tile,
                config=pack.manifest.storyteller,
                event_pool=pack.events,
                rng=random.Random(seed ^ hash(tile.tile_id)),
            )

    def _event_template(self, pack_id: str, kind: str):
        pack = self.packs.get(pack_id)
        if pack is None:
            return None
        return pack.events.get(kind)

    def _process_event(self, event: LegendEvent, stats: TickStats) -> None:
        """Unified event pipeline: enhance → record → persist → promote → memory → consequences."""
        t = self.world.current_tick
        log = self.tick_logger

        # Tier routing
        self.narrator.enhance(event)
        if log:
            tier_method = "template"
            if event.tier_used == 2:
                tier_method = "llm-rewrite"
            elif event.tier_used == 3:
                tier_method = "llm-dialogue"
            log.tier_routing(event.event_id, event.tier_used, tier_method)

        # Record
        self.world.record_event(event)
        stats.events_realized += 1
        if event.importance >= 0.6:
            stats.spotlight_candidates += 1

        # Persist
        if self.repository is not None:
            try:
                self.repository.append_event(event)
            except Exception as exc:  # noqa: BLE001
                print(f"[persist] append_event failed: {exc}")

        # Promote historical figures
        promoted = self.hf_registry.observe_event(event)
        stats.promotions += len(promoted)
        if log:
            for agent_id in promoted:
                log.promotion(agent_id, f"observed {event.event_kind}")

        # Memory — store the event in each participant's memory stream.
        # When subjective perception is available AND the event matters, the
        # LLM rewrites the memory from each agent's POV before storing.
        # Otherwise we store the objective best_rendering.
        if self.memory is not None and event.importance >= 0.1:
            use_subjective = (
                self.perception is not None
                and event.importance >= self.perception_threshold
            )
            for aid in event.participants:
                doc = event.best_rendering()
                if use_subjective:
                    agent = self.world.get_agent(aid)
                    if agent is not None:
                        doc = self.perception.reframe(agent, event)
                self.memory.remember(
                    agent_id=aid, tick=t,
                    doc=doc,
                    kind="subjective" if use_subjective else "raw",
                    importance=event.importance,
                    metadata={"event_id": event.event_id, "kind": event.event_kind},
                )
                if log:
                    log.memory_store(aid, "subjective" if use_subjective else "raw",
                                     event.importance)

        # ── CONSEQUENCES: stat changes + rare description mutations ──
        # No recursion — changes persist on agents, next tick reacts naturally.
        for reaction in self.consequences.apply(event):
            self.narrator.enhance(reaction)
            self.world.record_event(reaction)
            stats.reactions += 1
            if reaction.importance >= 0.6:
                stats.spotlight_candidates += 1
            self.hf_registry.observe_event(reaction)
            if log:
                changes = ", ".join(f"{k}:{v}" for d in reaction.stat_changes.values() for k, v in d.items())
                for aid in reaction.participants:
                    log.consequence(reaction.event_kind, aid, changes or "mutation")

        # ── AgentSelfUpdate: LLM speaks as participant, reports inner shift ──
        # Replaces the rigidness of pre-authored stat ripples for important
        # events. For each participant, the LLM emits a structured delta:
        # attribute changes, needs, emotions, possibly a new goal, beliefs.
        # The validator clamps everything before applying.
        if (
            self.self_update is not None
            and event.importance >= self.self_update_threshold
        ):
            for aid in event.participants:
                agent = self.world.get_agent(aid)
                if agent is None or not agent.is_alive():
                    continue
                applied = self.self_update.apply(agent, event)
                if not applied:
                    continue
                if log:
                    log.self_update(aid, applied)
                # If the LLM produced a reflection, store it as memory.
                refl = applied.get("reflection")
                if refl and self.memory is not None:
                    self.memory.remember(
                        agent_id=aid, tick=t, doc=refl,
                        kind="reflection", importance=event.importance,
                        metadata={"from_event": event.event_id},
                    )

        # ── Dialogue loop: A→B reaction mutates affinity + beliefs ──
        # For Tier-3 events with exactly two participants, ask the LLM to
        # generate the listener's in-character reaction (reply + affinity
        # delta + optional belief update). This is the one place where LLM
        # output mutates game state rather than just narrative.
        if (
            self.conversation_loop_enabled
            and event.tier_used >= 2
            and len(event.participants) == 2
            and self.dialogue_generator is not None
        ):
            speaker = self.world.get_agent(event.participants[0])
            listener = self.world.get_agent(event.participants[1])
            if speaker is not None and listener is not None and listener.is_alive():
                try:
                    reaction = self.dialogue_generator.conversation_turn(
                        speaker, listener, event, self.world, memory_store=self.memory,
                    )
                except Exception:
                    reaction = None
                if reaction:
                    delta = int(reaction.get("affinity_delta", 0) or 0)
                    belief = reaction.get("belief_update")
                    if delta:
                        listener.adjust_affinity(speaker.agent_id, delta, t)
                    if isinstance(belief, str) and belief.strip():
                        listener.set_belief(speaker.agent_id, belief)
                        if log:
                            log.belief_updated(listener.agent_id, speaker.agent_id, belief)
                    if log:
                        log.dialogue_reaction(
                            listener.agent_id, speaker.agent_id,
                            delta, reaction.get("reply", ""), belief,
                        )

        # ── Mark plans stale when reality disrupts them (re-plans next tick) ──
        # High-importance events invalidate the involved agents' plans; they
        # re-plan at the START of the next tick instead of waiting a week.
        if self.agent_planner is not None and event.importance >= 0.5:
            for aid in event.participants:
                agent = self.world.get_agent(aid)
                if agent is not None and agent.is_alive():
                    agent.state_extra["plan_stale"] = True
            # If someone's "seek" target just died, their plan is stale too.
            if event.outcome == "failure" or "death" in event.event_kind.lower():
                dead_ids = set(event.participants)
                for a in self.world.living_agents():
                    plan = a.get_weekly_plan() if hasattr(a, "get_weekly_plan") else {}
                    seek = plan.get("seek") if isinstance(plan, dict) else None
                    if isinstance(seek, list) and any(
                        any(d in str(s).lower() for d in dead_ids) for s in seek
                    ):
                        a.state_extra["plan_stale"] = True

    def _replan_stale_agents(self, t: int) -> int:
        """Regenerate weekly plans for agents marked stale by recent events."""
        if self.agent_planner is None:
            return 0
        count = 0
        for agent in list(self.world.living_agents()):
            if not agent.state_extra.get("plan_stale"):
                continue
            if self.plan_hf_only and not agent.is_historical_figure:
                agent.state_extra["plan_stale"] = False
                continue
            plan = self.agent_planner.plan_for_agent(agent, self.world, self.memory)
            if plan:
                agent.state_extra["weekly_plan"] = plan
                agent.state_extra["weekly_plan_tick"] = t
                if self.tick_logger:
                    self.tick_logger.plan_generated(agent.agent_id, plan)
                count += 1
            agent.state_extra["plan_stale"] = False
        return count

    def step(self) -> TickStats:
        self.world.current_tick += 1
        t = self.world.current_tick
        stats = TickStats(tick=t)
        log = self.tick_logger

        if log:
            log.tick_start(t)

        # ── Decay: needs grow, emotions normalize toward baseline (rule) ──
        decay_needs_and_emotions(self.world)

        # ── Re-plan agents whose plans became stale last tick ──
        self._replan_stale_agents(t)

        # ── Movement phase ──
        moves = self.movement.tick()
        stats.movements = len(moves)
        if log:
            for agent_id, from_tile, to_tile, method in moves:
                log.movement(agent_id, from_tile, to_tile, method)

        # ── Emergent interactions (lethal encounters, companionship, flight) ──
        for emergent in self.interactions.tick():
            if log:
                log.interaction(
                    emergent.event_kind, emergent.participants,
                    emergent.outcome, emergent.tile_id,
                )
            self._process_event(emergent, stats)

        # ── Storyteller-proposed events ──
        for tile_id, st in self.storytellers.items():
            proposals = st.tick_daily(t)
            stats.proposals += len(proposals)
            for prop in proposals:
                if log:
                    log.storyteller_proposal(tile_id, prop.event_kind, prop.priority)
                template = self._event_template(prop.pack_id, prop.event_kind)
                if template is None:
                    continue
                event = self.resolver.realize(
                    prop, template, t, consciousness=self.consciousness,
                )
                if event is None:
                    continue
                if log:
                    log.event_resolved(
                        event.event_id, event.event_kind, event.tile_id,
                        event.participants, event.outcome,
                        roll=None, dc=None,  # resolver doesn't expose these yet
                        importance=event.importance,
                        conscious_verdict=None,
                    )
                self._process_event(event, stats)

        # ── Emergent events: LLM invents novel events on hot tiles ──
        # For top-K hottest tiles (co-located + charged with affinity), ask
        # the LLM to invent ONE novel event grounded in who is present and
        # how they feel. Budget-gated by emergent_max_per_tick.
        if self.emergent_proposer is not None:
            # Pure-rule scoring picks WHICH tiles are hot; the LLM only acts
            # on the chosen one — keeps the agents/ layer focused.
            hot = _rule_hot_tiles(self.world, limit=self.emergent_max_per_tick)
            for tile in hot:
                event = self.emergent_proposer.propose(tile, self.world)
                if event is None:
                    continue
                if log:
                    log.emergent_event(
                        event.event_kind, event.tile_id, event.participants,
                    )
                self._process_event(event, stats)

        # periodic demotion (every 7 days)
        if t % 7 == 0:
            self.hf_registry.demote_inactive(t)

        # ── Chronicler (说书人) ──
        # Every 14 ticks, read the last window of events per pack and record
        # a chapter. This is DESCRIPTIVE ONLY — never steers future behavior.
        if (
            self.chronicler is not None
            and t - self._last_chronicle_tick >= self.chronicle_every_ticks
        ):
            for pack_id in self.world.loaded_packs:
                chapter = self.chronicler.write_chapter(
                    self.world, pack_id, since_tick=max(1, self._last_chronicle_tick + 1),
                )
                if chapter is not None:
                    self.world.add_chapter({
                        "tick": chapter.tick,
                        "pack_id": chapter.pack_id,
                        "title": chapter.title,
                        "body": chapter.body,
                        "event_ids": chapter.event_ids,
                    })
                    if self.tick_logger:
                        self.tick_logger.chapter_written(chapter.tick, chapter.title)
            self._last_chronicle_tick = t

        # ── Weekly agent planning ──
        # LLM generates a short plan for each (HF) agent. Stored on
        # state_extra["weekly_plan"] and consumed by movement + storyteller.
        if self.agent_planner is not None and t % self.plan_every_ticks == 0:
            targets = (
                list(self.world.historical_figures())
                if self.plan_hf_only
                else [a for a in self.world.living_agents()]
            )
            for agent in targets:
                plan = self.agent_planner.plan_for_agent(agent, self.world, self.memory)
                if plan:
                    agent.state_extra["weekly_plan"] = plan
                    agent.state_extra["weekly_plan_tick"] = t
                    if log:
                        log.plan_generated(agent.agent_id, plan)

        # periodic reflection (only for historical figures to save embed calls)
        if self.memory is not None and t % self.reflect_every_ticks == 0:
            for agent in self.world.historical_figures():
                self.memory.reflect(agent.agent_id, t)

        # periodic full-world snapshot
        if self.repository is not None and t % self.snapshot_every_ticks == 0:
            try:
                self.repository.save_world(self.world)
            except Exception as exc:  # noqa: BLE001
                print(f"[persist] snapshot failed: {exc}")

        stats.tier1 = self.narrator.stats.tier1
        stats.tier3 = self.narrator.stats.tier3

        if log:
            log.tick_end(t, stats)

        return stats

    def run(self, days: int) -> list[TickStats]:
        return [self.step() for _ in range(days)]
